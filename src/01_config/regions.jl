function in_region(name, lat, lon)
    lon = mod(lon, 360.0)
    lon_rng, lat_rng = regions[name]
    return lon_rng[1] <= lon && lon < lon_rng[2] && lat_rng[1] <= lat && lat < lat_rng[2] 
end
